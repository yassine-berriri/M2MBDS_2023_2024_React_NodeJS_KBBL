function Page(props) {
  /* --------------------------------------------------------------------
   *                               Props                                |
   * --------------------------------------------------------------------
   */
  const { pageName, children } = props;
  const className = props.className ? `Page ${props.className}` : "Page";
  const componentName = props.componentName
    ? `Page ${props.componentName}`
    : "Page";
  /* --------------------------------------------------------------------
   *                              States                                |
   * --------------------------------------------------------------------
   */
  /* --------------------------------------------------------------------
   *                             Functions                              |
   * --------------------------------------------------------------------
   */
  /* --------------------------------------------------------------------
   *                            Effect Hooks                            |
   * --------------------------------------------------------------------
   */
  useEffect(() => {
    window.scroll(0,0);
  }, [])

  /* --------------------------------------------------------------------
   *                                 JSX                                |
   * --------------------------------------------------------------------
   */

  return (
    <div
      className={className}
      project-component={componentName}
      project-page={pageName}
    >
      {children}
    </div>
  );
}

export default Page;
